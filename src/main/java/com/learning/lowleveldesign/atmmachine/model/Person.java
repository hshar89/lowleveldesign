package com.learning.lowleveldesign.atmmachine.model;

public class Person {
  private String fname;
  private String lname;
  private String email;
  private String phone;

  public Person(String fname, String lname, String email, String phone) {
    this.fname = fname;
    this.lname = lname;
    this.email = email;
    this.phone = phone;
  }
}
